package demo;

public class Datatype_eg
{
public static void main(String args[])
  {
	//boolean
	boolean flag=true;
	System.out.println("Value of flag= " +flag);
	
	//byte
	byte range;
	range=123;
	System.out.println("Value of range= " +range);
	
	//short
	short hahdg;
	hahdg=32767;
	System.out.println("Value of hahdg= " +hahdg);
	
	//int
	int yyb;
	yyb=89;
	System.out.println("Value of yyb= " +yyb);
	
	//long
	long oop;
	oop=9658965L;
	System.out.println("Value of oop= " +oop);
	
	//float
	float yui;
	yui=2345.62589f;
	System.out.println("Value of yui= " +yui);
	
	//double
	double ghj;
	ghj=582693.247895263;
	System.out.println("Value of ghj= " +ghj);
	
	//char
	char asd;
	asd='J';
	System.out.println("Value of asd= " +asd);

	}
}
