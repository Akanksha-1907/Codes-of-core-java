package demo;

public class datatypes1 
{
	public static void main(String[] args) 
	{
		// byte data type-
		byte a=56;
		byte b=49;
		int c=a+b;
		System.out.println("Addition of numbers- " +c);
		
		// int data type-
		int p=1000;
		int q=50;
		int r=p/q;
		System.out.println("Division of numbers- " +r);
		
		// long data type-
		long f=856974569321l;
		long u=58742300259l;
		long e=f*u;
		System.out.println("Multiplication of numbers- " +e);
		
		//float data type-
		float d=789568.369f;
		float z=87599.2f;
		float g=d/z;
		System.out.println("Division of numbers- " +g);
		
		//double data type-
		double l=7863258.25689d;
		System.out.println(l);
		
		//char data type-
		char x='A';
		char j='T';
		System.out.println("First character- " +x + " & Second Character- " +j);
		
		//short data type-
		short h=607;
		short v=61;
		int o=h%v;
		System.out.println("Modulo of numbers- " +o);
		
		//boolean data type-
		boolean flag=false;
		System.out.println("value is- " +flag);
		
	}

}
