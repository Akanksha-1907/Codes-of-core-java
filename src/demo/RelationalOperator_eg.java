package demo;

public class RelationalOperator_eg
{
	public static void main(String args[])
	{
		int a=89;
		int b=111;
		System.out.println("value of a= " +a+ " and value of b=" +b);
		
		//equal to(==)
		System.out.println(a==b);
		
		//less than(<)
		System.out.println(a<b);
		
		//greater than(>)
		System.out.println(a>b);
		
		//less than equal to(<=)
		System.out.println(a<=b);
		
		//greater than equal to(>=)
		System.out.println(a>=b);
		
		//not equal to(!=)
		System.out.println(a!=b);

	}

}
