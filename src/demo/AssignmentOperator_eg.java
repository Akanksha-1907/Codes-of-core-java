package demo;

public class AssignmentOperator_eg 
{
	public static void main(String args[])
	{
		int a=8;
		int var;
		var=a;
		System.out.println(var);
		var=var+a;
		System.out.println("Addition value " +var);
		var-=a;
		System.out.println("Subtraction value " +var );
		var=var*a;
		System.out.println("Multiplication value " +var);
		var/=a;
		System.out.println("Division value " +var);
	
	
	}
}
