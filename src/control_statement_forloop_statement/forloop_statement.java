package control_statement_forloop_statement;

public class forloop_statement 
{
	//program to print number from 1 to 5
	//starting with 1 i.e. initialization
	//check end point condition i.e. 5
	//here,post-increment
	
	//Syntax---->for(initialization;condition;increment/decrement operator)
	
	public static void main(String[] args) 
	{
		
	for(int a=1;a<=5;a++)
	{
		System.out.println(a);
	}

}
}
