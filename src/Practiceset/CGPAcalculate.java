package Practiceset;

public class CGPAcalculate 
{
	public static void main(String args[])
	{
		int m1=87;
		int m2=81;
		int m3=68;
		int m=m1+m2+m3;
		
		//Addition of marks of three subjects
		System.out.println(m);
		
		//Percentage of three subjects
		float n=300f;
		float p=100f;
		float e=((m/n)*p);
		System.out.println(e);
		
		//CGPA of three subjects
		float j=9.5f;
		System.out.println(e/j);
		
	}

}
